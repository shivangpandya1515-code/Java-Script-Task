// - Create an array `letters` with values `['b', 'c']`. Use `unshift` to add `'a'` to the beginning of the array.

let letters = ['b','c'];
console.log(letters);

letters.unshift('a');
console.log(letters);

// - Use `shift` to remove the first element from the `letters` array.

letters.shift();
console.log(letters);